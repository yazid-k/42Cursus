#include "../../minirt.h"

t_coord	vec_cross(t_coord v1, t_coord v2)
{
	return (coord(v1.y * v2.z - v1.z * v2.y,
			v1.z * v2.x - v1.x * v2.z,
			v1.x * v2.y - v1.y * v2.x));
}
